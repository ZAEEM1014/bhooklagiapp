class Restaurant {
  final String name;
  final String image;
  final double rating;
  final int reviews;
  final String category;
  final String deliveryTime;
  final String offer;
  final bool isAd;
  final bool freeDelivery;

  Restaurant({
    required this.name,
    required this.image,
    required this.rating,
    required this.reviews,
    required this.category,
    required this.deliveryTime,
    required this.offer,
    required this.isAd,
    required this.freeDelivery,
  });
}
